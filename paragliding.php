    <p>Paragliding is the recreational and competitive adventure sport of flying paragliders: lightweight, free-flying, foot-launched glider aircraft with no rigid primary structure. The pilot sits in a harness suspended below a fabric wing. Wing shape is maintained by the suspension lines, the pressure of air entering vents in the front of the wing, and the aerodynamic forces of the air flowing over the outside.</p>

    <p>Despite not using an engine, paraglider flights can last many hours and cover many hundreds of kilometres, though flights of one to two hours and covering some tens of kilometres are more the norm. By skillful exploitation of sources of lift, the pilot may gain height, often climbing to altitudes of a few thousand metres.</p>

    <div align="center" class="post-sub-title"><h3>History of Paragliding</h3></div><br>

    <p>In 1952 Canadian Domina Jalbert patented a governable gliding parachute with multi-cells and controls for lateral glide.In 1954, Walter Neumark predicted (in an article in Flight magazine) a time when a glider pilot would be "able to launch himself by running over the edge of a cliff or down a slope ... whether on a rock-climbing holiday in Skye or ski-ing in the Alps."In 1961, the French engineer Pierre Lemongine produced improved parachute designs that led to the Para-Commander. The PC had cutouts at the rear and sides that enabled it to be towed into the air and steered, leading to parasailing/parascending. Domina Jalbert invented the Parafoil, which had sectioned cells in an aerofoil shape; an open leading edge and a closed trailing edge, inflated by passage through the air – the ram-air design. He filed US Patent 3131894 on January 10, 1963.</p>

    <p>These developments were combined in June 1978 by three friends, Jean-Claude Bétemps, André Bohn and Gérard Bosson, from Mieussy, Haute-Savoie, France. After inspiration from an article on slope soaring in the Parachute Manual magazine by parachutist and publisher Dan Poynter, they calculated that on a suitable slope, a "square" ram-air parachute could be inflated by running down the slope; Bétemps launched from Pointe du Pertuiset, Mieussy, and flew 100 m. Bohn followed him and glided down to the football pitch in the valley 1000 metres below."Parapente" (pente being French for "slope") was born. From the 1980s, equipment has continued to improve, and the number of paragliding pilots and established sites has continued to increase. The first (unofficial) Paragliding World Championship was held in Verbier, Switzerland, in 1987, though the first officially sanctioned FAI World Paragliding Championship was held in Kössen, Austria, in 1989. Europe has seen the greatest growth in paragliding, with France alone registering in 2011 over 25,000 active pilots.</p>

