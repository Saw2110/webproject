    <div align="center" class="post-sub-title"><h3>History of Hiking</h3></div><br>

    <p>The idea of taking a walk in the countryside for pleasure developed in the 18th century, and arose because of changing attitudes to the landscape and nature associated with the Romantic movement.In earlier times walking generally indicated poverty and was also associated with vagrancy.</p>

    <p>Thomas West, an English priest, popularized the idea of walking for pleasure in his guide to the Lake District of 1778. In the introduction he wrote that he aimed to encourage the taste of visiting the lakes by furnishing the traveller with a Guide; and for that purpose, the writer has here collected and laid before him, all the select stations and points of view, noticed by those authors who have last made the tour of the lakes, verified by his own repeated observations.To this end he included various 'stations' or viewpoints around the lakes, from which tourists would be encouraged to enjoy the views in terms of their aesthetic qualities.Published in 1778 the book was a major success.</p>

